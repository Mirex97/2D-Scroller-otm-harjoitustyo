
package VR.mapitems;


public class HoboSpawnpoint {
    private double x;
    private double y;
    
    
    public HoboSpawnpoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return this.x;
    }
    public double getY() {
        return this.y;
    }
}
